import enum

from backend.config import settings

engine = create_engine(
    settings.DATABASE_URL,
    connect_args={"check_same_thread": False} if "sqlite" in settings.DATABASE_URL else {}
)


# Audit HIGH #8 (2026-06-05): WAL mode lets readers (eg the FastAPI /api
# routes serving the dashboard) coexist with the scheduler's writers without
# locking up the DB. busy_timeout=5000 means SQLite will sleep up to 5s on a
# lock contention before raising — eliminates spurious "database is locked"
# errors from short overlap between scan/settlement/dashboard transactions.
# Listener fires on every new dbapi connection (and pool checkouts re-use
# existing connections — listener does not re-run), so this is one-time per
# connection. Safe to leave unconditional; PRAGMA on non-SQLite no-ops are
# avoided because this listener is only attached for SQLite URLs.
if "sqlite" in settings.DATABASE_URL:
    @event.listens_for(engine, "connect")
    def _set_sqlite_pragmas(dbapi_conn, _):
        cur = dbapi_conn.cursor()
        cur.execute("PRAGMA journal_mode=WAL")
        cur.execute("PRAGMA busy_timeout=5000")
        cur.close()


SessionLocal = sessionmaker(autocommit=False, autoflush=False, bind=engine)
Base = declarative_base()


class Trade(Base):
    """Simulated trades for tracking P&L."""
    __tablename__ = "trades"

    id = Column(Integer, primary_key=True, index=True)
    signal_id = Column(Integer, index=True)
    market_ticker = Column(String, index=True)
    platform = Column(String)
    event_slug = Column(String, nullable=True)
    market_type = Column(String, default="btc", index=True)  # "btc" or "weather"

    # Trade details
    direction = Column(String)  # "up" or "down"
    entry_price = Column(Float)
    size = Column(Float)
    timestamp = Column(DateTime, default=datetime.utcnow)

    # Settlement
    settled = Column(Boolean, default=False)
    settlement_time = Column(DateTime, nullable=True)
    settlement_value = Column(Float, nullable=True)  # 1.0=Up won, 0.0=Down won
    result = Column(String, default="pending")  # pending, win, loss
    pnl = Column(Float, nullable=True)

    # Model performance tracking
    model_probability = Column(Float)
    market_price_at_entry = Column(Float)
    edge_at_entry = Column(Float)
    # Realistic-fills hybrid (2026-07-20): how the paper trade was filled —
    # 'instant' (swept at entry) or 'rest_fill' (a resting order later crossed).
    fill_type = Column(String, nullable=True)


