class ModelBias(Base):
    """Per-(city, model) additive daily-HIGH bias (forecast − ERA5 actual at the
    bot's coords, GMT frame — the model-upgrade groundwork frame). Refreshed
    nightly; bias_f is 0.0 (uncorrected) until n_days >= 20, and clamped ±5°F."""
    __tablename__ = "model_bias"
    id = Column(Integer, primary_key=True)
    city = Column(String, index=True)
    model = Column(String, index=True)
    bias_f = Column(Float, default=0.0)
    n_days = Column(Integer, default=0)
    window_days = Column(Integer, default=30)
    updated_at = Column(DateTime, default=datetime.utcnow)


class ForecastLog(Base):
    """Persist-forward daily-high forecast per (city, model, target_date). The
    ensemble API's past_days is unreliable (sparse members), so the bias window
    is filled by logging each night's forecast and joining to ERA5 actuals as
    they become available. One row per (city, model, target_date) — latest wins."""
    __tablename__ = "forecast_log"
    id = Column(Integer, primary_key=True)
    city = Column(String, index=True)
    model = Column(String, index=True)
    target_date = Column(String, index=True)   # ISO 'YYYY-MM-DD'
    forecast_high = Column(Float)
    recorded_at = Column(DateTime, default=datetime.utcnow)


class PaperRestingOrder(Base):
    """Simulated resting BUY order for the paper realistic-fills hybrid (2026-07-20).
    When an instant sweep leaves an unfilled remainder, we rest it at the entry
    limit price; later scans fill it if the real book's best_ask crosses down to
    <= our rest price (proxy for a seller hitting our bid — conservative, same
    spirit as the live take+rest). Cancelled (rest_expired) at settlement approach."""
    __tablename__ = "paper_resting_orders"
    id = Column(Integer, primary_key=True, index=True)
    signal_id = Column(Integer, nullable=True, index=True)
    market_ticker = Column(String, index=True)
    platform = Column(String, default="polymarket")
    event_slug = Column(String, nullable=True)
    condition_id = Column(String, nullable=True)
    direction = Column(String)                  # "yes" | "no"
    rest_price = Column(Float)                   # entry limit (cap) we rest at
    remaining_shares = Column(Float)             # unfilled shares still resting
    city_name = Column(String, nullable=True)
    target_date = Column(String, index=True)     # ISO 'YYYY-MM-DD' (settlement day)
    model_probability = Column(Float, nullable=True)
    market_price_at_entry = Column(Float, nullable=True)
    edge_at_entry = Column(Float, nullable=True)
    status = Column(String, default="resting", index=True)  # resting|filled|expired
    created_at = Column(DateTime, default=datetime.utcnow)
    updated_at = Column(DateTime, default=datetime.utcnow)


class CryptoWindow(Base):
    """CRYPTO5050 paper book (2026-07-22) — one row per traded 5-min BTC
    Up/Down window. COMPLETELY separate from the weather ledger: the module
    never writes to `trades`, and nothing here feeds the graduation stats.
    Maker fills use the optimistic crossed-bid proxy (queue unmodeled) — see
    backend/core/crypto5050.py docstring."""
    __tablename__ = "crypto_windows"
    id = Column(Integer, primary_key=True, index=True)
    slug = Column(String, unique=True, index=True)      # btc-updown-5m-<epoch>
    window_start = Column(DateTime, index=True)
    question = Column(String, nullable=True)
    up_token = Column(String, nullable=True)
    down_token = Column(String, nullable=True)
    up_shares = Column(Float, default=0.0)
    up_cost = Column(Float, default=0.0)                # ex-fee
    down_shares = Column(Float, default=0.0)
    down_cost = Column(Float, default=0.0)              # ex-fee
    fills_count = Column(Integer, default=0)
    maker_fills = Column(Integer, default=0)            # honesty meter numerator
    taker_fills = Column(Integer, default=0)
    pair_vwap = Column(Float, nullable=True)            # up VWAP + down VWAP
    locked_pairs = Column(Float, nullable=True)
    locked_pnl = Column(Float, nullable=True)
    lean_side = Column(String, nullable=True)           # "up" | "down"
    lean_shares = Column(Float, default=0.0)
    lean_price = Column(Float, nullable=True)
    lean_cost = Column(Float, default=0.0)              # ex-fee
    lean_pnl = Column(Float, nullable=True)
    pick_spot_drift = Column(String, nullable=True)     # LIVE lean rule's pick
    pick_momentum = Column(String, nullable=True)       # shadow rule (a)
    pick_depth = Column(String, nullable=True)          # shadow rule (b)
    pick_late_recency = Column(String, nullable=True)   # shadow rule (c): spot move over the FINAL ~60s (rotation/recency thesis, 7/22 article)
    pick_brownian = Column(String, nullable=True)       # shadow rule (d): brownian-gated (gengar gates) — up/down/abstain
    p_up_brownian = Column(Float, nullable=True)        # the P(Up) estimate behind the pick (review audit)
    hit_spot_drift = Column(Integer, nullable=True)     # 1/0/None per window
    hit_momentum = Column(Integer, nullable=True)
    hit_depth = Column(Integer, nullable=True)
    hit_late_recency = Column(Integer, nullable=True)
    hit_brownian = Column(Integer, nullable=True)       # abstain/no-data excluded from n
    arb_polls = Column(Integer, nullable=True)          # instant-arb visibility (item 8): book polls with both asks
    arb_hits = Column(Integer, nullable=True)           # polls where ask(Up)+ask(Down) < $1.00
    arb_best_sum = Column(Float, nullable=True)         # lowest observed ask-sum
    spot_open = Column(Float, nullable=True)
    spot_t60 = Column(Float, nullable=True)             # first spot sample inside the final ~60s (late-recency baseline)
    spot_close = Column(Float, nullable=True)
    resolution = Column(String, nullable=True)          # "up" | "down"
    resolution_source = Column(String, nullable=True)   # gamma | spot_fallback
    resolved_at = Column(DateTime, nullable=True)
    fees_paid = Column(Float, default=0.0)
    net_pnl = Column(Float, nullable=True)
    status = Column(String, default="open", index=True) # open|closing|settled|unresolved|skipped
    created_at = Column(DateTime, default=datetime.utcnow)


