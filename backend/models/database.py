def init_db():
    """Initialize database tables."""
    Base.metadata.create_all(bind=engine)
    ensure_schema()


def ensure_schema():
    """Ensure newer schema fields exist even if migration wasn't run."""
    inspector = inspect(engine)
    try:
        columns = [col["name"] for col in inspector.get_columns("trades")]
    except Exception:
        return

    if "event_slug" not in columns:
        stmt = "ALTER TABLE trades ADD COLUMN event_slug VARCHAR"
        if engine.dialect.name not in ("sqlite", "mysql"):
            stmt = "ALTER TABLE trades ADD COLUMN IF NOT EXISTS event_slug VARCHAR"

        with engine.connect() as conn:
            with conn.begin():
                conn.execute(text(stmt))

    if "market_type" not in columns:
        with engine.connect() as conn:
            with conn.begin():
                conn.execute(text("ALTER TABLE trades ADD COLUMN market_type VARCHAR DEFAULT 'btc'"))

    if "fill_type" not in columns:   # realistic-fills hybrid (2026-07-20)
        try:
            with engine.connect() as conn:
                with conn.begin():
                    conn.execute(text("ALTER TABLE trades ADD COLUMN fill_type VARCHAR"))
        except Exception:
            pass

    # Add calibration columns to signals table
    try:
        signal_columns = [col["name"] for col in inspector.get_columns("signals")]
    except Exception:
        signal_columns = []

    if signal_columns:
        with engine.connect() as conn:
            for col, coltype in [
                ("actual_outcome", "TEXT"),
                ("outcome_correct", "BOOLEAN"),
                ("settlement_value", "FLOAT"),
                ("settled_at", "DATETIME"),
                ("market_type", "VARCHAR DEFAULT 'btc'"),
                # Model-upgrade v1 shadow columns (2026-07-01, audit model-v1).
                ("model_probability_v2", "FLOAT"),
                ("ensemble_mean_v2", "FLOAT"),
                ("ensemble_std_v2", "FLOAT"),
                ("bias_applied_json", "TEXT"),
            ]:
                if col not in signal_columns:
                    try:
                        with conn.begin():
                            conn.execute(text(f"ALTER TABLE signals ADD COLUMN {col} {coltype}"))
                    except Exception:
                        pass  # column already exists

    # Model-upgrade v1 tables (2026-07-01): create if missing. create_all in
    # init_db already handles new tables, but do it defensively here too so an
    # older DB that skips init_db still gets them (idempotent).
    try:
        Base.metadata.create_all(bind=engine, tables=[ModelBias.__table__, ForecastLog.__table__])
    except Exception:
        pass

    # Realistic-fills resting-order table (2026-07-20): create if missing.
    try:
        Base.metadata.create_all(bind=engine, tables=[PaperRestingOrder.__table__])
    except Exception:
        pass

    # crypto5050 late-recency shadow rule (2026-07-22 PM): back-fill the three
    # new columns onto an existing crypto_windows table. NOTE: no per-poll spot
    # history exists, so prior windows CANNOT be backfilled with picks — the
    # late-recency series starts at deploy.
    try:
        cw_cols = [c["name"] for c in inspect(engine).get_columns("crypto_windows")]
        with engine.connect() as conn:
            with conn.begin():
                if "pick_late_recency" not in cw_cols:
                    conn.execute(text("ALTER TABLE crypto_windows ADD COLUMN pick_late_recency VARCHAR"))
                if "hit_late_recency" not in cw_cols:
                    conn.execute(text("ALTER TABLE crypto_windows ADD COLUMN hit_late_recency INTEGER"))
                if "spot_t60" not in cw_cols:
                    conn.execute(text("ALTER TABLE crypto_windows ADD COLUMN spot_t60 FLOAT"))
                # brownian rule + instant-arb visibility (2026-07-22 PM, items 7+8)
                if "pick_brownian" not in cw_cols:
                    conn.execute(text("ALTER TABLE crypto_windows ADD COLUMN pick_brownian VARCHAR"))
                if "p_up_brownian" not in cw_cols:
                    conn.execute(text("ALTER TABLE crypto_windows ADD COLUMN p_up_brownian FLOAT"))
                if "hit_brownian" not in cw_cols:
                    conn.execute(text("ALTER TABLE crypto_windows ADD COLUMN hit_brownian INTEGER"))
                if "arb_polls" not in cw_cols:
                    conn.execute(text("ALTER TABLE crypto_windows ADD COLUMN arb_polls INTEGER"))
                if "arb_hits" not in cw_cols:
                    conn.execute(text("ALTER TABLE crypto_windows ADD COLUMN arb_hits INTEGER"))
                if "arb_best_sum" not in cw_cols:
                    conn.execute(text("ALTER TABLE crypto_windows ADD COLUMN arb_best_sum FLOAT"))
    except Exception:
        pass

    # Resting-order signal_id link (2026-07-21): graduation-ledger traceability.
    # create_all above already gives a FRESH table the column; this idempotent
    # ALTER back-fills an existing paper_resting_orders table that predates it.
    try:
        ro_cols = [c["name"] for c in inspect(engine).get_columns("paper_resting_orders")]
        if "signal_id" not in ro_cols:
            with engine.connect() as conn:
                with conn.begin():
                    conn.execute(text("ALTER TABLE paper_resting_orders ADD COLUMN signal_id INTEGER"))
    except Exception:
        pass


def get_db():
    """Get database session."""
    db = SessionLocal()
    try:
        yield db
    finally:
        db.close()
