class ModelBias(Base):
    """Per-(city, model) additive daily-HIGH bias (forecast − ERA5 actual at the
    bot's coords, GMT frame — the model-upgrade groundwork frame). Refreshed
    nightly; bias_f is 0.0 (uncorrected) until n_days >= 20, and clamped ±5°F."""
    __tablename__ = "model_bias"
    id = Column(Integer, primary_key=True)
    city = Column(String, index=True)
    model = Column(String, index=True)
    bias_f = Column(Float, default=0.0)
    n_days = Column(Integer, default=0)
    window_days = Column(Integer, default=30)
    updated_at = Column(DateTime, default=datetime.utcnow)


class ForecastLog(Base):
    """Persist-forward daily-high forecast per (city, model, target_date). The
    ensemble API's past_days is unreliable (sparse members), so the bias window
    is filled by logging each night's forecast and joining to ERA5 actuals as
    they become available. One row per (city, model, target_date) — latest wins."""
    __tablename__ = "forecast_log"
    id = Column(Integer, primary_key=True)
    city = Column(String, index=True)
    model = Column(String, index=True)
    target_date = Column(String, index=True)   # ISO 'YYYY-MM-DD'
    forecast_high = Column(Float)
    recorded_at = Column(DateTime, default=datetime.utcnow)


class PaperRestingOrder(Base):
    """Simulated resting BUY order for the paper realistic-fills hybrid (2026-07-20).
    When an instant sweep leaves an unfilled remainder, we rest it at the entry
    limit price; later scans fill it if the real book's best_ask crosses down to
    <= our rest price (proxy for a seller hitting our bid — conservative, same
    spirit as the live take+rest). Cancelled (rest_expired) at settlement approach."""
    __tablename__ = "paper_resting_orders"
    id = Column(Integer, primary_key=True, index=True)
    signal_id = Column(Integer, nullable=True, index=True)
    market_ticker = Column(String, index=True)
    platform = Column(String, default="polymarket")
    event_slug = Column(String, nullable=True)
    condition_id = Column(String, nullable=True)
    direction = Column(String)                  # "yes" | "no"
    rest_price = Column(Float)                   # entry limit (cap) we rest at
    remaining_shares = Column(Float)             # unfilled shares still resting
    city_name = Column(String, nullable=True)
    target_date = Column(String, index=True)     # ISO 'YYYY-MM-DD' (settlement day)
    model_probability = Column(Float, nullable=True)
    market_price_at_entry = Column(Float, nullable=True)
    edge_at_entry = Column(Float, nullable=True)
    status = Column(String, default="resting", index=True)  # resting|filled|expired
    created_at = Column(DateTime, default=datetime.utcnow)
    updated_at = Column(DateTime, default=datetime.utcnow)


def init_db():
    """Initialize database tables."""
    Base.metadata.create_all(bind=engine)
    ensure_schema()


def ensure_schema():
    """Ensure newer schema fields exist even if migration wasn't run."""
    inspector = inspect(engine)
    try:
        columns = [col["name"] for col in inspector.get_columns("trades")]
    except Exception:
        return

    if "event_slug" not in columns:
        stmt = "ALTER TABLE trades ADD COLUMN event_slug VARCHAR"
        if engine.dialect.name not in ("sqlite", "mysql"):
            stmt = "ALTER TABLE trades ADD COLUMN IF NOT EXISTS event_slug VARCHAR"

        with engine.connect() as conn:
            with conn.begin():
                conn.execute(text(stmt))

    if "market_type" not in columns:
        with engine.connect() as conn:
            with conn.begin():
                conn.execute(text("ALTER TABLE trades ADD COLUMN market_type VARCHAR DEFAULT 'btc'"))

    if "fill_type" not in columns:   # realistic-fills hybrid (2026-07-20)
        try:
            with engine.connect() as conn:
                with conn.begin():
                    conn.execute(text("ALTER TABLE trades ADD COLUMN fill_type VARCHAR"))
        except Exception:
            pass

    # Add calibration columns to signals table
    try:
        signal_columns = [col["name"] for col in inspector.get_columns("signals")]
    except Exception:
        signal_columns = []

    if signal_columns:
        with engine.connect() as conn:
            for col, coltype in [
                ("actual_outcome", "TEXT"),
                ("outcome_correct", "BOOLEAN"),
                ("settlement_value", "FLOAT"),
                ("settled_at", "DATETIME"),
                ("market_type", "VARCHAR DEFAULT 'btc'"),
                # Model-upgrade v1 shadow columns (2026-07-01, audit model-v1).
                ("model_probability_v2", "FLOAT"),
                ("ensemble_mean_v2", "FLOAT"),
                ("ensemble_std_v2", "FLOAT"),
                ("bias_applied_json", "TEXT"),
            ]:
                if col not in signal_columns:
                    try:
                        with conn.begin():
                            conn.execute(text(f"ALTER TABLE signals ADD COLUMN {col} {coltype}"))
                    except Exception:
                        pass  # column already exists

    # Model-upgrade v1 tables (2026-07-01): create if missing. create_all in
    # init_db already handles new tables, but do it defensively here too so an
    # older DB that skips init_db still gets them (idempotent).
    try:
        Base.metadata.create_all(bind=engine, tables=[ModelBias.__table__, ForecastLog.__table__])
    except Exception:
        pass

    # Realistic-fills resting-order table (2026-07-20): create if missing.
    try:
        Base.metadata.create_all(bind=engine, tables=[PaperRestingOrder.__table__])
    except Exception:
        pass


def get_db():
    """Get database session."""
    db = SessionLocal()
    try:
        yield db
    finally:
        db.close()
